/**
 * add - function to add 2 numbers together 
 * @a: first value (int)
 * @b: second value (int) 
 * Return: Returns the sum 
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function to subtract a numbers from another
 * @a: first value (int)
 * @b: second value (int)
 * Return: Returns the difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function to find the product of 2 numbers
 * @a: first value (int)
 * @b: second value (int)
 * Return: Returns the product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function to divide a number by another
 * @a: first value (int)
 * @b: second value (int)
 * Return: Returns a divided by b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function to return the remainder from a division
 * @a: first value (int)
 * @b: second value (int)
 * Return: Returns the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
