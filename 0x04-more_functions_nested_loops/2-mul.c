/**
  * mul - Function to carry out multiplication of two numbers
  * @num_one: first numeric parameter supplied
  * @num_two: second numeric parameter supplied
  * Return: returns the product of @num_one and @num_two
  */

int mul(int num_one, int num_two)
{
	long int result;

	result = num_one * num_two;
	return (result);
}
